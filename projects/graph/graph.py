    def dft(self, starting_vertex):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        """
        stack = Stack()
        visited = set()
        stack.push(starting_vertex)
        while stack.size() > 0:
            vertex = stack.pop()
            if vertex not in visited:
                visited.add(vertex)
                print(vertex)
                for next_vert in self.vertices[vertex]:
                    stack.push(next_vert)

    def dft_recursive(self, starting_vertex, visited=None):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        This should be done using recursion.
        """
        if visited is None:
            visited = set()

        visited.add(starting_vertex)
        print(starting_vertex)

        for i_vert in self.vertices[starting_vertex]:
            if i_vert not in visited:
                self.dft_recursive(i_vert, visited)
        

        
    def bfs(self, starting_vertex, destination_vertex):
        """
        Return a list containing the shortest path from
        starting_vertex to destination_vertex in
        breath-first order.
        """
        visited = set()        
        qq = Queue()        
        qq.enqueue([starting_vertex])
        
        while qq.size() > 0:
            path = qq.dequeue()
            current = path[-1]
            if current not in visited:
                if current == destination_vertex:
                    return "BFS: "+ str(path)

                visited.add(current)
                
                for adjacents in self.vertices[current]:
                    new_path = list(path)
                    new_path.append(adjacents)
                    qq.enqueue(new_path)

        return "BFS: Value not found"
    def dfs(self, starting_vertex, destination_vertex):
        """
        Return a list containing a path from
        starting_vertex to destination_vertex in
        depth-first order.
        """
        # empty set to store visited
        visited = set()
        # create empty stack
        ss = Stack()
        # path starting_vortex to stack
        ss.push([starting_vertex])
        # while stack is not empty:
        while ss.size() > 0:
            # pop out first path
            path = ss.pop()
            current = path[-1]
            # if not visited. add it to visited:
            if current not in visited:
                # if vertex = target:
                if current == destination_vertex:
                # return path
                    return "DFS: "+ str(path)
            # mark it as visited
            visited.add(current)
            # add a path to all adjacents to the stack:
            for adjacents in self.vertices[current]:
                # copy the path
                copy_path = list(path)
                # add adjacent to back of the copy 
                copy_path.append(adjacents)
                ss.push(copy_path)
        return "DFS: Value not found"




